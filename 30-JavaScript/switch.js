let day="Goodday";

switch(day){
    case "Monday":
        console.log("Oh no!Its Monday!");
        break;
    case "Tueday":
        console.log("Atleast its not Monday!");
        break;
    case "Wednesday":
        console.log("Midweek! Halfway there!");
        break;
    case "Thursday":
        console.log("Almost Friday!");
        break;
    case "Friday":
        console.log("Yay!Its Friday!");
        break;
    case "Saturday":
        console.log("Weekend Vibes!");
        break;
    case "Sunday":
        console.log("Relax!Its still the weekend!");
        break;
    default:
        console.log("Just another day or invalid day!")
}